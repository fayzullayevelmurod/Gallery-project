window.addEventListener('load', () => {
  // Swiper tugmalarini topish
  const buttons = document.querySelectorAll('.swiper-button-prev, .swiper-button-next');

  buttons.forEach(button => {
    button.removeAttribute('disabled');

    button.classList.remove('swiper-button-lock');
  });
});


try {
  // Cookie banner elementlarini tanlash
  const cookieBanner = document.querySelector('.cookie');
  const closeCookieButton = document.querySelector('.close-cookie');

  // Cookie holatini tekshirish
  if (!localStorage.getItem('cookieAccepted')) {
    // Cookie bannerini ko‘rsatish
    cookieBanner.style.display = 'flex';
  }

  // "Accept" tugmasi bosilganda
  closeCookieButton.addEventListener('click', () => {
    // Cookie qabul qilindi holatini saqlash
    localStorage.setItem('cookieAccepted', 'true');
    // Cookie bannerini yashirish
    cookieBanner.style.display = 'none';
  });
} catch (error) {

}
